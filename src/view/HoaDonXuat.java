     private void showHDX() {
        ArrayList<HDXModel> ivs = HDXCtrl.showHDX();
        DefaultTableModel model = (DefaultTableModel) HDXTb.getModel();
        for (HDXModel iv : ivs) {
            Object[] row = {iv.getId(),iv.getCusId() , iv.getDate() };
            model.addRow(row);
        }
    }
